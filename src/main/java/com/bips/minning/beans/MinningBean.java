package com.bips.minning.beans;

public class MinningBean {
private int noOfHoles;
private int feet;
private float onefeetrate;

private String date;
private int totalfeet;
private float total;

public int getNoOfHoles() {
	return noOfHoles;
}
public void setNoOfHoles(int noOfHoles) {
	this.noOfHoles = noOfHoles;
}
public int getFeet() {
	return feet;
}
public void setFeet(int feet) {
	this.feet = feet;
}
public int getTotalfeet() {
	return totalfeet;
}
public void setTotalfeet(int totalfeet) {
	this.totalfeet = totalfeet;
}
public float getOnefeetrate() {
	return onefeetrate;
}
public void setOnefeetrate(float onefeetrate) {
	this.onefeetrate = onefeetrate;
}
public float getTotal() {
	return total;
}
public void setTotal(float total) {
	this.total = total;
}
public String getDate() {
	return date;
}
public void setDate(String date) {
	this.date = date;
}

}
